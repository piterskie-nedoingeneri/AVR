void input(byte inp)
{
	start();
	byte buff=0x80;
	while(buff!=0x00)
	{
		
		PORT1&=~(1<<CLK);
		if((buff&inp)!=0x00)
		{
			PORT1|=1<<DIN;
			
		}
		else
		{
			PORT1&=~(1<<DIN);
		}
		
		//PORTA&=0b11110111;
		PORT1|=1<<CLK;
		buff>>=1;
	}
	stop();
}
